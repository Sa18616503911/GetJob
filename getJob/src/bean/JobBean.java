package bean;

public class JobBean {

	private String jobName;
	private String enable;
	private String dbConfigId;
	private String srcDb;
	private String srcTable;
	private String desDb;
	private String desTable;
	private String keyType;
	private String empty;
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getEnable() {
		return enable;
	}
	public void setEnable(String enable) {
		this.enable = enable;
	}
	public String getDbConfigId() {
		return dbConfigId;
	}
	public void setDbConfigId(String dbConfigId) {
		this.dbConfigId = dbConfigId;
	}
	public String getSrcDb() {
		return srcDb;
	}
	public void setSrcDb(String srcDb) {
		this.srcDb = srcDb;
	}
	public String getSrcTable() {
		return srcTable;
	}
	public void setSrcTable(String srcTable) {
		this.srcTable = srcTable;
	}
	public String getDesDb() {
		return desDb;
	}
	public void setDesDb(String desDb) {
		this.desDb = desDb;
	}
	public String getDesTable() {
		return desTable;
	}
	public void setDesTable(String desTable) {
		this.desTable = desTable;
	}
	public String getKeyType() {
		return keyType;
	}
	public void setKeyType(String keyType) {
		this.keyType = keyType;
	}
	public String getEmpty() {
		return empty;
	}
	public void setEmpty(String empty) {
		this.empty = empty;
	}
	
	
}
